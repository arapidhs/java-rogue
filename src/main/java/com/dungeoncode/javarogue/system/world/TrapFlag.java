package com.dungeoncode.javarogue.system.world;

/**
 * Enum representing trap types in the Rogue game, equivalent to trap type definitions in rogue.h.
 */
public enum TrapFlag {
    T_DOOR,    // Trap at door
    T_ARROW,   // Arrow trap
    T_SLEEP,   // Sleep trap
    T_BEAR,    // Bear trap
    T_TELEP,   // Teleport trap
    T_DART,    // Dart trap
    T_RUST,    // Rust trap
    T_MYST     // Mystery trap
}
